DROP DATABASE IF EXISTS benchp;
CREATE DATABASE benchp;
USE benchp;
CREATE TABLE t (id INT PRIMARY KEY, v INT, name VARCHAR(20));
CREATE PROCEDURE fill() BEGIN DECLARE i INT; DECLARE nm VARCHAR(20); SET i = 0; WHILE i < 20000 DO SET nm = CONCAT('row', i); INSERT INTO t VALUES (i, i, nm); SET i = i + 1; END WHILE; END;
BEGIN;
CALL fill();
COMMIT;
SELECT COUNT(*) AS total FROM t;
SELECT COUNT(*) AS eq7 FROM t WHERE v = 7;
SELECT COUNT(*) AS rng FROM t WHERE v > 50 AND id < 10000;
SELECT COUNT(*) AS likecnt FROM t WHERE name LIKE 'row1%';
